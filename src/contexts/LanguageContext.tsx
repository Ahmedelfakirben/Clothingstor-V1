export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [currentLanguage, setCurrentLanguage] = useState<Language>('es');
  const [isLoading, setIsLoading] = useState(true);

  // Cargar idioma desde la base de datos al iniciar
  useEffect(() => {
    const loadLanguageFromDB = async () => {
      try {

        const { data, error } = await supabase
          .from('company_settings')
          .select('language')
          .single();

        if (error) {
          console.error('‚ùå [LANGUAGE] Error loading language from DB:', error);
          // Si hay error, usar idioma por defecto
          setCurrentLanguage('es');

        } else if (data && data.language) {
          console.log(`‚úÖ [LANGUAGE] Loaded language from DB: ${data.language}`);
          setCurrentLanguage(data.language as Language);
          document.documentElement.lang = data.language;
        } else {
          // Si no hay idioma configurado, usar espa√±ol por defecto
          console.log('‚ö†Ô∏è [LANGUAGE] No language configured, using default: es');
          setCurrentLanguage('es');
        }
      } catch (error) {
        console.error('üí• [LANGUAGE] Error loading language:', error);
        setCurrentLanguage('es');
      } finally {
        setIsLoading(false);
      }
    };

    loadLanguageFromDB();

    // Suscribirse a cambios en tiempo real en company_settings
    console.log('üì° [LANGUAGE] Setting up Realtime subscription...');
    const channel = supabase
      .channel('company-settings-language')
      .on(
        'postgres_changes',
        {
          event: 'UPDATE',
          schema: 'public',
          table: 'company_settings'
        },
        (payload) => {
          console.log('üîî [LANGUAGE] Realtime change detected!', payload);
          if (payload.new && (payload.new as any).language) {
            const newLanguage = (payload.new as any).language as Language;
            console.log(`üåç [LANGUAGE] Updating language to: ${newLanguage} (from Realtime)`);
            setCurrentLanguage(newLanguage);
            document.documentElement.lang = newLanguage;
            console.log(`‚úÖ [LANGUAGE] Language updated successfully to: ${newLanguage}`);
          }
        }
      )
      .subscribe((status) => {
        console.log(`üì° [LANGUAGE] Realtime subscription status: ${status}`);
      });

    return () => {
      console.log('üîå [LANGUAGE] Disconnecting Realtime subscription...');
      supabase.removeChannel(channel);
    };
  }, []);

  const setLanguage = async (lang: Language) => {
    try {
      console.log(`üåç [LANGUAGE] Attempting to change language to: ${lang}`);

      // Primero obtener el ID de company_settings
      const { data: settingsData, error: fetchError } = await supabase
        .from('company_settings')
        .select('id')
        .single();

      if (fetchError) {
        console.error('‚ùå [LANGUAGE] Error fetching company_settings ID:', fetchError);
        throw fetchError;
      }

      if (!settingsData) {
        console.error('‚ùå [LANGUAGE] No company_settings record found');
        throw new Error('No company_settings record found');
      }

      console.log(`üìù [LANGUAGE] Updating company_settings (ID: ${settingsData.id}) to language: ${lang}`);

      // Actualizar en la base de datos (esto notificar√° a todos los clientes conectados)
      const { error: updateError } = await supabase
        .from('company_settings')
        .update({ language: lang })
        .eq('id', settingsData.id);

      if (updateError) {
        console.error('‚ùå [LANGUAGE] Error updating language in DB:', updateError);
        throw updateError;
      }

      console.log(`‚úÖ [LANGUAGE] Language updated to ${lang} in database`);

      // Actualizar inmediatamente en el cliente
      setCurrentLanguage(lang);
      document.documentElement.lang = lang;

      console.log(`‚úÖ [LANGUAGE] Language updated to ${lang} in local state`);
    } catch (error) {
      console.error('üí• [LANGUAGE] Error setting language:', error);
      // Revertir el cambio cargando desde la base de datos
      const { data } = await supabase.from('company_settings').select('language').single();
      if (data && data.language) {
        setCurrentLanguage(data.language as Language);
        document.documentElement.lang = data.language;
      }
      throw error; // Re-lanzar el error para que AppSettings pueda manejarlo
    }
  };

  const t = (key: string): string => {
    return translations[currentLanguage][key as keyof typeof translations.es] || key;
  };

  // Mostrar un loader mientras se carga el idioma
  if (isLoading) {
    return null; // o un spinner si prefieres
  }

  return (
    <LanguageContext.Provider value={{ currentLanguage, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}